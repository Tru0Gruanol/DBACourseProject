-- 3h. 同步 amount_paid = SUM(accounts) 、 enrolled_count = COUNT(active)
UPDATE student_enrollments e
JOIN (SELECT student_id, class_code, SUM(amount_paid) AS total FROM accounts GROUP BY student_id, class_code) a
  ON e.student_id = a.student_id AND e.class_code = a.class_code
SET e.amount_paid = a.total WHERE e.status = 'active';

UPDATE classes c
LEFT JOIN (SELECT class_code, COUNT(*) AS cnt FROM student_enrollments WHERE status = 'active' GROUP BY class_code) e
  ON c.class_code = e.class_code
SET c.enrolled_count = COALESCE(e.cnt, 0);

